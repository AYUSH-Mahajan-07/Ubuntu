    private void WordEnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_WordEnterActionPerformed
    analyzeText(); // This will update basic stats and clear character count
    String word = SpecificWord.getText();
    if (!word.isEmpty()) {
        Practical13 analyzer = new Practical13(Screen.getText());
        int count = analyzer.getWordCount(word);
        WordCount.setText(String.valueOf(count));
    }
    }//GEN-LAST:event_WordEnterActionPerformed

