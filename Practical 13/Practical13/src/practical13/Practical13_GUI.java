    private void EnterForCharacterCountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EnterForCharacterCountActionPerformed
    String text = Screen.getText();
    if (!text.isEmpty()) {
        String selectedChar = (String)ComboBox.getSelectedItem();
        if (selectedChar != null && !selectedChar.isEmpty()) {
            Practical13 analyzer = new Practical13(text);
            int charCount = analyzer.getCharCount(selectedChar.charAt(0));
            CharacterCount.setText(String.valueOf(charCount));
        }
    }
    }//GEN-LAST:event_EnterForCharacterCountActionPerformed

