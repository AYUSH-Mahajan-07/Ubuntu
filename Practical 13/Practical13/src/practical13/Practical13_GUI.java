private void analyzeText() {
    String text = Screen.getText();
    Practical13 analyzer = new Practical13(text);
    
    // Update basic statistics
    Length.setText(String.valueOf(analyzer.getTextLength()));
    Lines.setText(String.valueOf(analyzer.getLineCount()));
    Tabs.setText(String.valueOf(analyzer.getTabCount()));
    Space.setText(String.valueOf(analyzer.getSpaceCount()));
    Words.setText(String.valueOf(analyzer.getWordCount()));
    
    // Clear character count until button is pressed
    CharacterCount.setText("");
}
